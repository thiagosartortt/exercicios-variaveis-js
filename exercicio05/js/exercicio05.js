function matematicaSoma() {
    let numero1 = parseFloat (prompt ("Digite um numero"));
    let numero2 = parseFloat (prompt ("Digite um numero"));
    
    let resultado = (numero1 + numero2).toFixed(2);
    
    alert("Resultado: " + resultado); 
}


